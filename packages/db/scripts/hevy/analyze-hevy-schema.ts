function analyzeObject(obj: Record<string, unknown>, prefix = ""): string[] {
  const lines: string[] = [];
  for (const [key, value] of Object.entries(obj)) {
    const path = prefix ? `${prefix}.${key}` : key;
    if (value === null) {
      lines.push(`- \`${path}\`: null`);
    } else if (Array.isArray(value)) {
      const sample = value[0];
      const inner =
        sample && typeof sample === "object"
          ? `object { ${Object.keys(sample as object).join(", ")} }`
          : typeof sample;
      lines.push(`- \`${path}\`: array<${inner ?? "unknown"}> (length ${value.length})`);
    } else {
      lines.push(`- \`${path}\`: ${typeof value}${typeof value === "string" ? ` e.g. "${value.slice(0, 40)}"` : ""}`);
    }
  }
  return lines;
}

